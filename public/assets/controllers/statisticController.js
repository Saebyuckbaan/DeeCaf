function queryUserHistory()
{
  var currentUser = Parse.User.current();
  var historyObj;
  if( currentUser )
  {
    var intakeHistory = currentUser.get("intakeHistory");
    var dateArray = [];
    var intakeArray = [];
    var date;
    $.each( intakeHistory, function( index, value ){ 
      date = new Date(value["date"]);
      dateArray.push( date.getDate() );
      intakeArray.push( value["intake"] );

    });

    historyObj = { "dateArray": dateArray, "intakeArray": intakeArray };
    debugger;

    console.log("Date Array = " + dateArray);
    console.log("intake Array = " + intakeArray);
    return historyObj;
  }
}